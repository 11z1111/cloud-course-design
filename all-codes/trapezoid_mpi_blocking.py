#!/usr/bin/env python3
"""
梯形法数值积分 - MPI 并行版
使用 Scatter 分发子区间，Reduce 求和
计算 ∫(x^2) dx from a to b
"""

from mpi4py import MPI

def f(x):
    """被积函数 f(x) = x^2"""
    return x * x

def trapezoidal_serial(a, b, n):
    """单个进程的局部积分计算（梯形法）"""
    h = (b - a) / n
    integral = (f(a) + f(b)) / 2.0
    for i in range(1, n):
        x = a + i * h
        integral += f(x)
    integral *= h
    return integral

if __name__ == "__main__":
    # ============================================================
    # MPI 通信原语1：MPI.COMM_WORLD - 获取全局通信域
    # ============================================================
    # 作用：获取 MPI 默认通信域，包含所有进程
    # 数据流向：无数据传输，初始化 MPI 环境
    # ============================================================
    comm = MPI.COMM_WORLD
    
    # ============================================================
    # MPI 通信原语2：comm.Get_rank() - 获取当前进程编号
    # ============================================================
    # 作用：返回当前进程在通信域中的唯一标识符
    # 数据流向：本地操作，无跨进程通信
    # ============================================================
    rank = comm.Get_rank()
    
    # ============================================================
    # MPI 通信原语3：comm.Get_size() - 获取总进程数
    # ============================================================
    # 作用：返回通信域中的总进程数量
    # 数据流向：本地操作，无跨进程通信
    # ============================================================
    size = comm.Get_size()
    
    # 全局参数
    a, b = 0.0, 1.0          # 积分区间 [0, 1]
    n_total = 1000000        # 总子区间数
    
    # ============================================================
    # MPI 通信原语4：Scatter（隐式实现）- 分发子区间
    # ============================================================
    # 作用：将全局区间 [a, b] 均匀分割成 size 份，每个进程获得一份
    # 数据流向：每个进程根据 rank 自动计算自己的子区间起止点
    #   rank0: [0.00, 0.25)
    #   rank1: [0.25, 0.50)
    #   rank2: [0.50, 0.75)
    #   rank3: [0.75, 1.00]
    # ============================================================
    h_global = (b - a) / size           # 每个进程的区间宽度
    local_a = a + rank * h_global       # 本进程的区间起点
    local_b = local_a + h_global        # 本进程的区间终点
    
    local_n = n_total // size           # 每个进程的子区间数
    if rank == size - 1:                # 最后一个进程处理余数
        local_n = n_total - (size - 1) * local_n
    
    # ============================================================
    # 局部计算（无通信）
    # ============================================================
    # 每个进程独立计算自己子区间的积分
    # 数据流向：无跨进程通信，各自独立计算
    # ============================================================
    local_integral = trapezoidal_serial(local_a, local_b, local_n)
    
    # ============================================================
    # MPI 通信原语5：comm.reduce() - 归约求和
    # ============================================================
    # 作用：将所有进程的 local_integral 通过求和操作归约到 root 进程
    # op=MPI.SUM: 归约操作类型（求和）
    # root=0: 目标进程编号（结果汇总到进程0）
    #
    # 数据流向：
    #   进程0: local_integral ──┐
    #   进程1: local_integral ──┼──→ 进程0
    #   进程2: local_integral ──┤      total_integral = sum(all)
    #   进程3: local_integral ──┘
    # ============================================================
    total_integral = comm.reduce(local_integral, op=MPI.SUM, root=0)
    
    # ============================================================
    # 结果输出（仅 root 进程）
    # ============================================================
    if rank == 0:
        print(f"并行版: ∫({a}→{b}) x^2 dx = {total_integral:.11f}")
